                    </div>
                    <div class="col col-x2">
                        <p>Mahamudra means &quot;The Great Seal&quot; and is the crown
                            jewel of Buddha’s teachings. When one rests at all
                            times in a state of non-duality, the practitioner has
                            reached the goal of experiencing mind’s natural
                            state – Mahamudra.</p>
                        <p>This state, also called enlightenment, brings forth
                            unconditioned fearlessness, joy and active
                            compassion in the practitioner’s mind. One then acts
                            spontaneously and effortlessly for the benefit of all.</p>
