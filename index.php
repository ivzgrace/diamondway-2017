                            and limitless ..." – Lama Ole Nydahl</blockquote>
                    </div>
                    <br>
                    <div class="col">
                        <p>He has since transmitted the blessing of the lineage
                            in a powerful and contemporary way. He travels
                            continuously teaching worldwide.</p>
                        <p>He has established around 700 Diamond Way Buddhist
                            Centres that are now under the guidance of the
                            17th Gyalwa Karmapa Thaye Dorje in 44 mainly
                            Western countries.</p>
                        <p>Join him this Australian summer on the Gold Coast and
                            Ballina for meditation and teaching retreats and in Melbourne for a public talk.</p>
