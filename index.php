                        <p>Lama Ole Nydahl is one of the few Westerners fully
                            qualified as a Buddhist teacher (Lama) and meditation
                            master in the Tibetan Karma Kagyu Buddhist tradition.</p>
                        <p>In 1972, after completing three years of intensive
                            meditation training, Lama Ole and his wife Hannah
                            began teaching Vajrayana (Diamond Way) Buddhism in
                            Europe at the request of the 16th Gyalwa Karmapa,
                            Rangjung Rigpe Dorje, the spiritual head of the
                            Karma Kagyu school of Tibetan Buddhism.</p>
                    </div>
                    <div class="col col-x2 with-image">
